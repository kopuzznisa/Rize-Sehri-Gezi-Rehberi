const itemsPerPage = 12;
let currentPage = 1;
const totalItems = galleryItems.length;

// Sayfa numarasını URL'den veya localStorage'dan al
function getCurrentPage() {
    const urlParams = new URLSearchParams(window.location.search);
    const page = parseInt(urlParams.get('page')) || parseInt(localStorage.getItem('currentPage')) || 1;
    return page;
}

function setCurrentPage(page) {
    currentPage = page;
    localStorage.setItem('currentPage', page);
}

function paginate(array, pageSize, pageNumber) {
    return array.slice((pageNumber - 1) * pageSize, pageNumber * pageSize);
}

function loadGalleryItems() {
    const gallery = document.getElementById('gallery');
    gallery.innerHTML = '';

    const paginatedItems = paginate(galleryItems, itemsPerPage, currentPage);
    paginatedItems.forEach(item => {
        const galleryItem = document.createElement('div');
        galleryItem.classList.add('gallery-item');
        
        galleryItem.innerHTML = `
            <a href="${item.href}">
                <img src="${item.imgSrc}" alt="${item.alt}">
                <h3>${item.title}</h3>
            </a>
        `;

        gallery.appendChild(galleryItem);
    });

    // URL'ye sayfa parametresini ekle
    const newUrl = `${window.location.pathname}?page=${currentPage}`;
    window.history.pushState({ path: newUrl }, '', newUrl);
}

function loadPagination() {
    const pagination = document.getElementById('pagination');
    pagination.innerHTML = '';

    const totalPages = Math.ceil(totalItems / itemsPerPage);

    for (let i = 1; i <= totalPages; i++) {
        const button = document.createElement('button');
        button.textContent = i;
        button.classList.add('pagination-button');
        if (i === currentPage) {
            button.classList.add('active');
        }
        button.addEventListener('click', () => {
            setCurrentPage(i);
            loadGalleryItems();
            loadPagination();
        });

        pagination.appendChild(button);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    setCurrentPage(getCurrentPage());
    loadGalleryItems();
    loadPagination();

    const prevPageButton = document.getElementById('prevPage');
    const nextPageButton = document.getElementById('nextPage');

    prevPageButton.addEventListener('click', () => {
        if (currentPage > 1) {
            setCurrentPage(currentPage - 1);
            loadGalleryItems();
            loadPagination();
        }
    });

    nextPageButton.addEventListener('click', () => {
        if (currentPage < Math.ceil(totalItems / itemsPerPage)) {
            setCurrentPage(currentPage + 1);
            loadGalleryItems();
            loadPagination();
        }
    });
});

